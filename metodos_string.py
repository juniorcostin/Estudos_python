mensagem = "eu adoro comida Caseira"
#colocar um . após a variável string vai mostrar uma lista de metodos
#apresenta o dado em letra maiúscula
print(mensagem.upper())
#apresenta o dado em lentra minúscula
print(mensagem.lower())
#apresenta o dado com a primeira letra em maiúsculo
print(mensagem.capitalize())
#busca pela letra dentro do index, podem ser palavras
print(mensagem.find('c'))
#substitui a primeira informação pela segunda
print(mensagem.replace("a", "e"))
print(mensagem.replace("Caseira", "Feita em casa"))
#remove qualquer espaço antes do inicio da informação
print(mensagem.strip())

